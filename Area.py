
#Triangle
base = float(input("Enter Base: "))
height = float(input("Enter Height: "))

area = .5*base*height
print("Area Of a Triangle: ",area)

#Square
length = float(input("\nEnter length: "))

area = length**2
print("Area of a Sqaure: ",area)

#Rectangle
height = float(input("\nEnter height: "))
width = float(input("Enter width: "))

area = height*width
print("Area of a Rectangle: ",area)

#Trapezium
a = int(input("\nEnter a: "))
b = int(input("Enter b: "))
h = float(input("Enter Vertical Height: "))

area = .5*(a+b)*h
print("Area of a Trapezium: ",area)

#Circle

radius = float(input("\nEnter Radius: "))
area = 3.1416*radius
print("Area of a Radius: ",area)
